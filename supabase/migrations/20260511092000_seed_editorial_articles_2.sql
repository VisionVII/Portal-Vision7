<h2>Portugal e a Europa: O EU Chips Act em Contexto</h2>
<h3>A Aposta Europeia: €43 Mil Milhões</h3>
<p>O <strong>European Chips Act</strong>, aprovado em 2023, compromete <strong>€43 mil milhões</strong> para aumentar a quota de mercado europeia de semicondutores de 9% para 20% até 2030. O projecto mais visível: a <strong>Intel</strong> comprometeu-se a construir uma megafab em Magdeburg, Alemanha, com investimento de €17 mil milhões.</p>
<p>Em 2026, o projecto Intel na Alemanha está em construção, mas enfrenta atrasos e renegociações de subsídios. A meta de 20% até 2030 é vista por muitos analistas como optimista — mas a direcção é clara.</p>
<h3>O Papel da ASML na Estratégia Europeia</h3>
<p>A <strong>ASML</strong> é o activo mais valioso da Europa neste ecossistema — e a UE está ciente disso. As restrições de exportação às máquinas EUV são a principal alavanca de pressão europeia na geopolítica dos semicondutores.</p>
<h3>Três Cenários para Portugal e a Europa (2028)</h3>
<p><strong>Cenário Optimista (25% probabilidade)</strong>
O EU Chips Act cumpre os seus objectivos: a Europa atinge 15-18% de quota de mercado em chips avançados até 2028. A fab Intel em Magdeburg torna-se operacional e âncora um cluster tecnológico europeu. Portugal beneficia indirectamente através da integração nas cadeias de fornecimento europeias — especialmente em componentes de menor complexidade e software embarcado.</p>
<p><strong>Cenário Realista (55% probabilidade)</strong>
A Europa progride, mas mais devagar do que as metas. A Intel Magdeburg arranca em 2027-2028 com capacidade reduzida. A TSMC estabelece uma presença europeia limitada. Portugal mantém-se como utilizador de chips sem capacidade de produção própria, mas beneficia de um ecossistema europeu mais robusto e menos dependente de Taiwan para a segurança do fornecimento.</p>
<p><strong>Cenário Pessimista (20% probabilidade)</strong>
Tensão militar no estreito de Taiwan antes de 2028 perturba gravemente o fornecimento global de chips. A transição para produção fora de Taiwan acelera, mas não é suficientemente rápida para evitar uma recessão tecnológica global. Portugal e a Europa enfrentam escassez de chips que limita o crescimento digital e industrial.</p>
