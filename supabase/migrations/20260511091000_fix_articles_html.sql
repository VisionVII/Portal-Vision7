<h2>O Que É Um Agente de IA?</h2>
<p>Um <strong>agente de IA</strong> é um sistema que:</p>
<ol>
<li><strong>Percebe</strong> o ambiente — lê emails, analisa documentos, vê ecrãs, consulta APIs</li>
<li><strong>Raciocina</strong> sobre o que deve fazer — usando um modelo de linguagem como o Claude ou o GPT-4o</li>
<li><strong>Age</strong> no ambiente — envia emails, escreve código, clica em botões, chama serviços externos</li>
<li><strong>Observa</strong> o resultado e ajusta a abordagem para o passo seguinte</li>
</ol>
<p>A diferença fundamental em relação a um chatbot: um chatbot <strong>responde</strong>. Um agente <strong>executa</strong>.</p>
<p>Quando perguntas ao Claude &quot;como devo estruturar este email?&quot;, estás a usar um assistente. Quando o agente <strong>analisa a caixa de entrada, classifica os emails por urgência, redige rascunhos e envia as respostas aprovadas</strong> — isso é um agente.</p>
<hr>
<h2>Como Funcionam: O Loop de Raciocínio-Acção</h2>
<pre><code>PERCEPÇÃO
(Lê o ambiente: email, base de dados, ecrã, API)
         ↓
PLANEAMENTO
(O modelo de linguagem decide o próximo passo)
         ↓
ACÇÃO
(Executa: chama API, escreve ficheiro, navega na web)
         ↓
OBSERVAÇÃO
(Verifica o resultado — sucesso? erro? continua?)
         ↓
[Repete até concluir a tarefa ou atingir um limite]
</code></pre>
<p>Este loop — perceber, planear, agir, observar — é o coração de qualquer agente. Pode correr dezenas ou centenas de iterações para completar uma tarefa complexa. A qualidade do modelo de linguagem que raciocina em cada iteração determina a qualidade do agente.</p>
