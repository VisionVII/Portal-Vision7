<h2>Os Riscos Reais e Como Mitigá-los</h2>
<h3>Risco 1: Alucinações em Agentes Autónomos</h3>
<p>Um agente pode &quot;inventar&quot; dados e tomar acções incorrectas com base em premissas falsas. A mitigação: camadas de validação antes de acções irreversíveis, logs completos de todas as decisões, e supervisão humana nos pontos de maior risco.</p>
<h3>Risco 2: Dependência Excessiva</h3>
<p>Equipas que perdem a capacidade de executar as tarefas sem o agente criam um risco operacional sério. A mitigação: documentar todos os processos que os agentes executam, manter literacia processual nas equipas.</p>
<h3>Risco 3: Segurança e RGPD</h3>
<p>Agentes com acesso a dados pessoais ou sensíveis representam um vector de ataque e um risco de conformidade. A mitigação: princípio do mínimo privilégio (o agente acede apenas ao que precisa), auditoria completa, conformidade documentada com o RGPD e o EU AI Act.</p>
