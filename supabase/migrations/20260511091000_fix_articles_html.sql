<h2>Os Tipos de Agentes em 2026</h2>
<h3>Agentes de Tarefa Única</h3>
<p>Executam uma tarefa específica de forma autónoma e fiável. São os mais simples de implementar e os que têm maior ROI imediato. Exemplo: um agente que monitoriza o email, identifica pedidos de suporte e cria tickets no Zendesk automaticamente — sem revisão humana para os casos standard.</p>
<h3>Agentes Multi-Tarefa (Orquestrados)</h3>
<p>Coordenam múltiplas sub-tarefas para atingir um objectivo complexo. O <strong>n8n</strong> e o <strong>Make</strong> são as plataformas mais usadas para orquestrar estes agentes em Portugal.</p>
<p>Exemplo real — pipeline editorial automatizado:</p>
<ul>
<li>WF-01: Monitoriza 50 feeds RSS e recolhe notícias</li>
<li>WF-02: Agrupa artigos relacionados por similaridade semântica</li>
<li>WF-03: Gera artigo original com Claude Sonnet</li>
<li>WF-04: Monitoriza a qualidade e alertas</li>
<li>WF-05: Publica e distribui pelas redes sociais</li>
</ul>
